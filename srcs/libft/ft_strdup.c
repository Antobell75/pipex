#include "libft.h"

char	*ft_strdup(char *str)
{
	int		i;
	char	*cpy;

	i = 0;
	cpy = malloc((ft_strlen(str) + 1) * sizeof(char));
	if (cpy == NULL)
		return (NULL);
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
